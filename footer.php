        <script>
            //ローディング画面
            $(document).ready(function() {
                let images = $('img');
                let totalImages = images.length;
                let loadedImages = 0;

                if (totalImages === 0) {
                    // 画像がない場合はすぐにローディング画面を閉じる
                    $('#loading').addClass('slide-up');
                    setTimeout(function() {
                        $('#loading').hide();
                        $('#content').fadeIn('slow');
                    }, 600); // 0.6秒待つ
                    return;
                }

                images.each(function() {
                    let img = new Image();
                    img.src = $(this).attr('src');
                    img.onload = img.onerror = function() {
                        loadedImages++;
                        let progress = Math.floor((loadedImages / totalImages) * 100);

                        if (loadedImages === totalImages) {
                            setTimeout(function() {
                                $('.loading__inner').fadeOut();
                                setTimeout(function() {
                                    $('#loading').addClass('slide-up');
                                    $('#content').fadeIn('slow');
                                }, 400); // 0.6秒待つ
                            }, 600); // 0.6秒待つ
                        }
                    };
                });
            });
